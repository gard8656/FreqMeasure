static bool OnKey(Control event)
{
    if (event.value == Control::RegLeft)
    {
        FDrive::PressDown();
        return true;
    }
    else if (event.value == Control::RegRight)
    {
        FDrive::PressUp();
        return true;
    }

    return false;
}

